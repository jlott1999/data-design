<h1>Use Case/Interaction Flow</h1>
<p>Desiree goes on to the home page of eBay.</p>
<p>She clicks Browse by Category, and under Fashion, she clicks on shoes.</p>
<p>She clicks on Casual Shoes, and in the category Top Shoe Brands, she clicks Vans.</p>
<p>She clicks on Vans Slip On, and in the Price settings on the left, she edits the price range from $30-$50.</p>
<p>She scrolls through the first page, and doesn't see the shoes. She then continues to Page Two.</p>
<p>She scrolls down, and sees the shoes she is looking for, for $30! They are used however, so she clicks on it to
    check it out.</p>
<p>She looks at the pictures of the shoes, and they look almost new! The description says that they are barely
    used, and were only worn once, and the owner is selling them because he didn't like the shoes.</p>
<p>She saw that the seller has a 99% Positive Rating. Desiree now trusts the seller, and knows that the shoes will
be in great condition for the price she was asking! She continued to buy the shoes.</p>
